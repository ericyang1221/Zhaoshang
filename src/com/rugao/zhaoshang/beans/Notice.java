package com.rugao.zhaoshang.beans;

public class Notice {
	private int noticeId;
	private String noticeIdDisplay;
	private String createDate;

	public int getNoticeId() {
		return noticeId;
	}

	public void setNoticeId(int noticeId) {
		this.noticeId = noticeId;
	}

	public String getNoticeIdDisplay() {
		return noticeIdDisplay;
	}

	public void setNoticeIdDisplay(String noticeIdDisplay) {
		this.noticeIdDisplay = noticeIdDisplay;
	}

	public String getCreateDate() {
		return createDate;
	}

	public void setCreateDate(String createDate) {
		this.createDate = createDate;
	}

}
